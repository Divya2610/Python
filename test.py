import turtle 
paint=turtle.Turtle()
paint.speed(2)
paint.penup()
paint.goto(-50,60)
paint.pendown()
paint.color("#00adef")
paint.begin_fill()
paint.goto(100,100)
paint.goto(100,-100)
paint.goto(-50,-60)
paint.goto(-50,60)
paint.end_fill()
paint.color("black")
paint.goto(15,100)
paint.color("black")
paint.width(5)
paint.goto(15, -100)
paint.penup()
paint.goto(100, 0)
paint.pendown()
paint.goto(-100,0)
paint.done()
